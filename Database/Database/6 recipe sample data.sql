Potato Salad


•	8 medium potatoes
	 
3 eggs, hard boiled, sliced
•	  
2 carrots, grated
•	 
1 medium onion, grated
•	  
1/2 teaspoon salt
•	  
3 tablespoons lemon juice
•	  
1/2 cup mayonnaise


1.	Peel and boil potatoes. Drain and let cool, then cut into cubes.
2.	Combine potatoes with eggs, carrots, onion, and salt.
3.	Combine mayonnaise and lemon juice. Add to rest of ingredients. Toss well.
4.	Refrigerate overnight.

